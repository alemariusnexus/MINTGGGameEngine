Font Font::defaultFont(nullptr);

std::unordered_map<std::string, Font> Font::fontRegistry;



Font Font::loadFONTX2Static (
    const uint8_t* rawData, size_t fullSize,
    bool* ok, const char** outErrmsg
) {
    return loadFONTX2(rawData, fullSize, false, ok, outErrmsg);
}

Font Font::loadFONTX2File (
    const std::string_view& path,
    bool* ok, const char** outErrmsg
) {
    bool dummyOk;
    if (!ok) {
        ok = &dummyOk;
    }

    File f(path.data());
    if (!f.open(File::ReadOnly, outErrmsg)) {
        *ok = false;
        return {};
    }
    size_t fullSize = f.getSize();
    uint8_t* rawData = static_cast<uint8_t*>(malloc(fullSize));
    if (!rawData) {
        f.close();
        if (outErrmsg) *outErrmsg = "allocation failed";
        *ok = false;
        return {};
    }
    if (f.readAll(rawData, fullSize) != fullSize) {
        f.close();
        if (outErrmsg) *outErrmsg = "error reading file";
        *ok = false;
        return {};
    }
    f.close();
    Font font = loadFONTX2(rawData, fullSize, true, ok, outErrmsg);
    if (!*ok) {
        free(rawData);
    }
    return font;
}

void Font::setDefaultFont(const Font& font)
{
    defaultFont = font;
}

void Font::registerFont(const Font& font)
{
    fontRegistry[font.getName()] = font;
}

bool Font::isFontRegistered(const std::string_view& name)
{
    return fontRegistry.find(std::string(name)) != fontRegistry.end();
}

void Font::loadDefaultFonts()
{
    struct DefaultFont
    {
        const uint8_t* fontx2Data;
        size_t fontx2DataSize;
        std::string name;
    };

    const DefaultFont defaultFonts[] = {
        {   font5x8_ISO8859_1,      sizeof(font5x8_ISO8859_1),      "5x8_ISO8859_1"     },
        {   font6x12_ISO8859_1,     sizeof(font6x12_ISO8859_1),     "6x12_ISO8859_1"    },
        {   font8x13_ISO8859_1,     sizeof(font8x13_ISO8859_1),     "8x13_ISO8859_1"    },
        {   font9x15_ISO8859_1,     sizeof(font9x15_ISO8859_1),     "9x15_ISO8859_1"    }
    };

    size_t fontIdx = 0;
    for (const DefaultFont& df : defaultFonts) {
        bool ok;
        const char* errmsg;
        Font font = loadFONTX2Static(df.fontx2Data, df.fontx2DataSize, &ok, &errmsg);
        if (!ok) {
            LogError("Error loading default font %u: %s", fontIdx, errmsg);
        }
        font.setName(df.name);
        LogInfo("Loaded font: %s", font.getName().c_str());
        registerFont(font);
        if (fontIdx == 0) {
            setDefaultFont(font);
        }
        fontIdx++;
    }
}


Font Font::loadFONTX2 (
    const uint8_t* rawData,
    size_t fullSize,
    bool bufOwned,
    bool* ok,
    const char** outErrmsg
) {
    if (fullSize < 16) {
        // We don't even have a full header
        if (outErrmsg) *outErrmsg = "premature end of file";
        if (ok) *ok = false;
        return {};
    }

    if (memcmp(rawData, "FONTX2", 6) != 0) {
        if (outErrmsg) *outErrmsg = "invalid file signature";
        if (ok) *ok = false;
        return {};
    }

    const uint8_t glyphWidth = rawData[14];
    const uint8_t glyphHeight = rawData[15];

    const size_t glyphSize = ((glyphWidth + 7) / 8) * glyphHeight;

    const uint8_t codeFlag = rawData[16];

    if (codeFlag == 0) {
        // Single-byte format

        const size_t blockSize = 256*glyphSize;

        if (fullSize < 16+blockSize) {
            if (outErrmsg) *outErrmsg = "premature end of file";
            if (ok) *ok = false;
            return {};
        }
    } else {
        // Double-byte format

        if (fullSize < 17) {
            if (outErrmsg) *outErrmsg = "premature end of file";
            if (ok) *ok = false;
            return {};
        }

        const uint8_t numCodeBlocks = rawData[17];

        const uint8_t* blockPtr = rawData+17;
        size_t sizeLeft = fullSize-17;

        for (uint8_t i = 0 ; i < numCodeBlocks ; i++) {
            if (sizeLeft < 4) {
                if (outErrmsg) *outErrmsg = "premature end of file";
                if (ok) *ok = false;
                return {};
            }

            const uint16_t firstCP = FromLittleEndian(*reinterpret_cast<const uint16_t*>(blockPtr));
            const uint16_t lastCP = FromLittleEndian(*reinterpret_cast<const uint16_t*>(blockPtr+2));
            const size_t blockSize = (lastCP-firstCP+1)*glyphSize;

            if (sizeLeft < 4+blockSize) {
                if (outErrmsg) *outErrmsg = "premature end of file";
                if (ok) *ok = false;
                return {};
            }

            sizeLeft -= 4+blockSize;
            blockPtr += 4+blockSize;
        }
    }

    *ok = true;
    return Font(rawData, bufOwned);
}



Font::Font(const std::string_view& name)
{
    auto it = fontRegistry.find(std::string(name));
    if (it != fontRegistry.end()) {
        d = it->second.d;
    } else {
        d = defaultFont.d;
    }
}

Font::Font()
    : Font(defaultFont)
{
}

Font::Font(const uint8_t* rawData, bool bufOwned) : d(std::make_shared<Data>(rawData, bufOwned))
{
    const char* namePtr = reinterpret_cast<const char*>(d->rawData + 6);
    const char* nptr = static_cast<const char*>(memchr(namePtr, '\0', 8));
    d->name = nptr ? std::string(namePtr, (nptr-namePtr)) : std::string(namePtr, 8);
}

std::string Font::getName() const
{
    return d->name;
}

void Font::setName(const std::string_view& name)
{
    d->name = name;
}

uint8_t Font::getGlyphWidth() const
{
    return d->rawData[14];
}

uint8_t Font::getGlyphHeight() const
{
    return d->rawData[15];
}

size_t Font::getGlyphSize() const
{
    return ((d->rawData[14] + 7) / 8) * d->rawData[15];
}

const uint8_t* Font::getGlyphBuffer(uint16_t cp) const
{
    if (d->rawData[16] == 0) {
        // Single byte format
        if (cp > 255) {
            return nullptr;
        }
        return d->rawData + 17 + cp*getGlyphSize();
    } else {
        // Double byte format
        const uint8_t numCbs = d->rawData[17];
        const size_t glyphSize = getGlyphSize();

        const uint8_t* blockPtr = d->rawData + 18;

        for (uint8_t i = 0 ; i < numCbs ; i++) {
            const uint16_t firstCP = FromLittleEndian(*reinterpret_cast<const uint16_t*>(blockPtr));
            const uint16_t lastCP = FromLittleEndian(*reinterpret_cast<const uint16_t*>(blockPtr+2));
            if (cp >= firstCP  &&  cp <= lastCP) {
                return blockPtr + 4 + (cp-firstCP)*glyphSize;
            }
            blockPtr += (lastCP-firstCP+1)*glyphSize;
        }

        return nullptr;
    }
}


